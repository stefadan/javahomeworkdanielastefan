package tema5;

public class NumberHolder {
    public int anInt;
    public float aFloat;
    
    NumberHolder(int a, float b){
        anInt = a;
        aFloat = b;
    }

    public int getAnInt() {
        return anInt;
    }

    public float getaFloat() {
        return aFloat;
    }
    
}
